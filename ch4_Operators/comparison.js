// === is called strict eqyality opretor, it checks if the value and the type are the same. So in this case, age is a number and 6 is a number, but they are not equal, so it returns false.

 // | Operator | Meaning          |
// | -------- | ---------------- |
// | ==       | Equal            |
// | ===      | Strict equal     |
// | !=       | Not equal        |
// | !==      | Strict not equal |
// | >        | Greater than     |
// | <        | Less than        |
// | >=       | Greater or equal |
// | <=       | Less or equal    |


var age = 5

console.log(age != 5) // it means not equal, (which is not true!)


var age = 5

console.log(age > 5) // it means greater than, (which is not true!)


var age = 5

console.log(age < 5)  // it means less than, (which is not true!)

var age = 5

console.log(age >= 5) // it means greater than or equal to, (which is true!)


var age = 5

console.log(age <= 5) // it means less than or equal to, (which is true!)





